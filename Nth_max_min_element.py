## Program to find minimum and maximum element

##for minimum
def minimum():
    lst=[10,5,75,36,86,54,6,7,65,12]
    lst.sort()
    print("Minimum element is: ",lst[0])
minimum()

##for maximum

def maximum():
    lst=[10,5,75,36,86,54,6,7,65,12]
    lst.sort()
    print("Maximum element is: ",lst[-1])
maximum()
